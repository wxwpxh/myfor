      PROGRAM PSSPAP
C     PLAN STRESS/STRAIN PROBLAM ANALYSIS PROGRAM
      DIMENSION LND(500,3),X(200),Y(200),JZ(50,3),
     &  PJ(50,3),P(500),TL(20),AK(500,100),AKE(6,6)
      OPEN (6,FILE='PSSPAP.IN')
      OPEN (8,FILE='PSSPAP.OUT')
      READ(6,10) TL
10    FORMAT(20A4)
      READ(6,*)NJ,NE,NZ,NPJ,IPS
      WRITE(8,10)TL
      IF(IPS.EQ.1) WRITE(8,20)
      IF(IPS.EQ.2) WRITE(8,30)
20    FORMAT(/1X,'PLATE STRESS PROBLEM')
30    FORMAT(/1X,'PLATE STRAIN PROBLEM')
      END
